#include    <iostream>
using namespace std;
int main(){
    //Type casting is converting one data type to another data type.
    //take intger value and print half of the value
    int a=45;    
    float b=(float)a;
     //type casting
    cout<<"The value of half of a is ";
    cout<<a/2<<endl; //without type casting
    cout<<"The value of half of a is ";
    cout<<b/2<<endl; //with type casting
}